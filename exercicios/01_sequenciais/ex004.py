# Exercício 004 - Dobro, triplo e raiz quadrada   

numero=float(input('Insira um numero: '))

dobro = numero*2
triplo = numero*3
raiz_quadrada = numero** 0.5

print(f"o dobro de {numero} é {dobro:.2f}\n"
      f"o triplo de {numero} é {triplo:.2f}\n"
      f"a raiz quadrada de {numero} é {raiz_quadrada:.2f}\n"
)